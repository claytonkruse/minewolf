<h1>About <span>Minewolf</span></h1>
<p>
	Minewolf is a Minecraft server list. Why did I make another Minecraft server list in a vast,
	unrelenting sea of server lists? Because I could do it better.
</p>
<p>
	Rather than having server owners manually enter things like the server version, and plugins, these
	things are tracked automatically. Not only does this save time for the server owners, it will
	allow players to more easily find servers they're interested in. Server information will be
	constantly up to date.
</p>
<p>
	The con that comes with this automatic style of server listing, is that many tags cannot be added
	manually. Proxy servers will not show the plugins of their subservers, unless programmed
	specifcally to do so. I am content with this. I would like to be the creator of a server list that
	favors smaller, non-proxied servers anyway.
</p>
<p>
	I also plan for Minewolf to be at least a <em>little</em> inovative, like all developers with
	delusions of of grandeur. So far I've got: <em>revenue sharing</em>.
</p>
<p>
	With revenue sharing, server owners would get a portion of the ad revenue from votes, and maybe
	even from their server page in the case that they can find a reason to send people there
	<em>frequently</em>
	enough. Revenue sharing will be difficult to do, so I'm planning on adding it late in the game,
	<em>if it happens at all.</em>
</p>
<p>
	A real optimistic idea I'm having is that I could somehow integrate donations and even become the
	next Buycraft, though I doubt that's happening.
</p>
